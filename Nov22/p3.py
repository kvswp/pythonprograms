
mydict = {'key1':'val1','key2':'val2'}
print("mydict[key1] = {}".format(mydict['key1']))

d = {'k1':['a','b','c'],'k2':[0,1,2],'k3':{'ins':10}}
print("{} {} {}".format(d['k1'], d['k2'], d['k3']))

print("d['k2'][2] = {}".format(d['k2'][2]))
print("d['k1'][1] in upper case = {}".format(d['k1'][1].upper()))

print("d.keys = {}".format(d.keys()))
print("d.values = {}".format(d.values()))
print("d.items = {}".format(d.items()))

